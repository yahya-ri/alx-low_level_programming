#include <stdio.h>

/**
 * main - prints its name followed by a new line.
 * @argc: number of command line arguments.
 * @argv: array that contains the program command line arguments.
 * Return: Always 0.
 */

int main(int argc, char *argv[] __attribute__((unused)))
{
    printf("%d\n", argc);
    return (0);
}
